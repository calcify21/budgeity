/**
 * Auto-generates TypeScript type definitions from your English translation JSON.
 * Run this script whenever you add/remove keys from en/translation.json:
 *   node scripts/generate-i18n-types.cjs
 *
 * This creates types/i18n.d.ts which gives you autocomplete and compile-time
 * validation for all t() calls.
 */
const fs = require("fs");
const path = require("path");

const enPath = path.join(
  process.cwd(),
  "public",
  "locales",
  "en",
  "translation.json",
);
const outputPath = path.join(process.cwd(), "types", "i18n.d.ts");

function flattenObj(obj, parent = "", res = {}) {
  for (let key in obj) {
    let propName = parent ? parent + "." + key : key;
    if (
      typeof obj[key] === "object" &&
      obj[key] !== null &&
      !Array.isArray(obj[key])
    ) {
      flattenObj(obj[key], propName, res);
    } else {
      res[propName] = obj[key];
    }
  }
  return res;
}

function generateTypeString(obj, indent = "    ") {
  if (typeof obj === "string") {
    return "string";
  }
  if (Array.isArray(obj)) {
    return "string[]";
  }
  if (typeof obj === "object" && obj !== null) {
    let parts = [];
    for (let key in obj) {
      let formattedKey = /^[a-zA-Z_$][a-zA-Z0-9_$]*$/.test(key) ? key : `"${key}"`;
      parts.push(`${indent}${formattedKey}: ${generateTypeString(obj[key], indent + "  ")};`);
    }
    return `{\n${parts.join("\n")}\n${indent.slice(2)}}`;
  }
  return "any";
}

const enContent = JSON.parse(fs.readFileSync(enPath, "utf8"));
const keys = Object.keys(flattenObj(enContent));

let output = `// Auto-generated by scripts/generate-i18n-types.cjs
// Do not edit manually. Run: node scripts/generate-i18n-types.cjs
//
// This file provides type-safe translation keys for react-i18next.
// After regenerating, your IDE will autocomplete t("...") calls
// and TypeScript will error on invalid keys.

import "i18next";

type TranslationKeys =
`;

// Build the union type
keys.forEach((key, i) => {
  output += `  | "${key}"`;
  output += i < keys.length - 1 ? "\n" : ";\n";
});

output += `
declare module "i18next" {
  interface CustomTypeOptions {
    defaultNS: "translation";
    resources: {
      translation: ${generateTypeString(enContent, "        ")};
    };
  }
}
`;

// Ensure types directory exists
const typesDir = path.dirname(outputPath);
if (!fs.existsSync(typesDir)) {
  fs.mkdirSync(typesDir, { recursive: true });
}

fs.writeFileSync(outputPath, output);
console.log(`✅ Generated ${keys.length} typed keys → ${outputPath}`);

